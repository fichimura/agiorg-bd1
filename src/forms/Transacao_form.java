    //PREENCHE TABELA COM TRANSACOES
    public void preencherTabela() {
        TransacaoDAO tdao = new TransacaoDAO();
        List<Transacao> listaTransacao = tdao.listarTodos();
        DefaultTableModel tabelaT = (DefaultTableModel) jTabela_transacao.getModel();
        tabelaT.setRowCount(0);
        for (Transacao t : listaTransacao) {
            tabelaT.addRow(new Object[]{t.getId_transacao(), t.getDescricao(), t.getQuantia(), t.getData_transacao(), t.getCategoria(), t.getFK_id_usuario()});
        }

    }

    //PREENCHE TABELA COM DATA PESQUISADA
    public void preencherTabelaData(String data) {
        TransacaoDAO tdao = new TransacaoDAO();
        List<Transacao> listaTransacao = tdao.buscarData(data);
        DefaultTableModel tabelaT = (DefaultTableModel) jTabela_transacao.getModel();
        tabelaT.setRowCount(0);
        listaTransacao.forEach(t -> {
            tabelaT.addRow(new Object[]{t.getId_transacao(), t.getDescricao(), t.getQuantia(), t.getData_transacao(), t.getCategoria(), t.getFK_id_usuario()});
        });

    }
    
    //PREENCHE TABELA COM CATEGORIA PESQUISADA
    public void preencherTabelaCategoria(String categoria) {
        TransacaoDAO tdao = new TransacaoDAO();
        List<Transacao> listaTransacao = tdao.buscarCategoria(categoria);
        DefaultTableModel tabelaT = (DefaultTableModel) jTabela_transacao.getModel();
        tabelaT.setRowCount(0);
        listaTransacao.forEach(t -> {
            tabelaT.addRow(new Object[]{t.getId_transacao(), t.getDescricao(), t.getQuantia(), t.getData_transacao(), t.getCategoria(), t.getFK_id_usuario()});
        });

    }

