    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int opcao = jTabela_transacao.getSelectedRow();
        if (opcao >= 0) {
            Transacao t = new Transacao();
            t.setId_transacao(Integer.parseInt(jTabela_transacao.getValueAt(opcao, 0).toString()));
            t.setDescricao(jTabela_transacao.getValueAt(opcao, 1).toString());
            t.setQuantia(Double.parseDouble(jTabela_transacao.getValueAt(opcao, 2).toString()));
            t.setData_transacao(jTabela_transacao.getValueAt(opcao, 3).toString());
            t.setCategoria(jTabela_transacao.getValueAt(opcao, 4).toString());
            t.setFK_id_usuario(Integer.parseInt(jTabela_transacao.getValueAt(opcao, 5).toString()));

            TransacaoDAO tdao = new TransacaoDAO();
            tdao.excluir(t, usuario);
            preencherTabela();

            if (t.getCategoria().equals("ENTRADA")) {
                usuario.setSaldoTotal(usuario.getSaldoTotal() - t.getQuantia());
            } else if (t.getCategoria().equals("SAIDA")) {
                usuario.setSaldoTotal(usuario.getSaldoTotal() + t.getQuantia());
            }
            UsuarioDAO udao = new UsuarioDAO();
            udao.atualizar(usuario);

            jL_saldo.setText("Saldo total: R$" + usuario.getSaldoTotal());

            this.getMf().dispose();

            Menu_form mfn = new Menu_form(usuario);
            mfn.setVisible(true);
            this.setVisible(true);

            jTabbedPane1.setSelectedIndex(1);
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha.");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

