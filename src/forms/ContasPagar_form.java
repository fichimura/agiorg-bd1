    //PREENCHE TABELA DE CONTAS
    public void preencherTabela() {
        ContasPagarDAO cdao = new ContasPagarDAO();
        List<ContasPagar> listaContas = cdao.listarTodos();
        DefaultTableModel tabelaC = (DefaultTableModel) jTabela_contas.getModel();
        tabelaC.setRowCount(0);
        for (ContasPagar c : listaContas) {
            tabelaC.addRow(new Object[]{c.getId_conta(), c.getDescricao(), c.getQuantia(), c.getData_conta(), c.getFK_id_usuario()});
        }
    }

