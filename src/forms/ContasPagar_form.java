    //BOTAO EDITAR
    private void jB_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_editarActionPerformed
        int opcao = jTabela_contas.getSelectedRow();
        if (opcao >= 0) {

            jT_id.setText((jTabela_contas.getValueAt(opcao, 0).toString()));
            jT_descricao.setText(jTabela_contas.getValueAt(opcao, 1).toString());
            jT_quantia.setText((jTabela_contas.getValueAt(opcao, 2).toString()));
            jF_data.setText(jTabela_contas.getValueAt(opcao, 3).toString());
            jT_idusuario.setText(jTabela_contas.getValueAt(opcao, 4).toString());

            jTabbedPane1.setSelectedIndex(0);
            jB_atualizar.setVisible(true);
            jB_salvar.setVisible(false);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha.");
        }
    }//GEN-LAST:event_jB_editarActionPerformed

    //BOTAO EXCLUIR
    private void jBut_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBut_excluirActionPerformed

        int opcao = jTabela_contas.getSelectedRow();
        if (opcao >= 0) {
            ContasPagar c = new ContasPagar();
            c.setId_conta(Integer.parseInt(jTabela_contas.getValueAt(opcao, 0).toString()));
            c.setDescricao(jTabela_contas.getValueAt(opcao, 1).toString());
            c.setQuantia(Double.parseDouble(jTabela_contas.getValueAt(opcao, 2).toString()));
            c.setData_conta(jTabela_contas.getValueAt(opcao, 3).toString());
            c.setFK_id_usuario(Integer.parseInt(jTabela_contas.getValueAt(opcao, 4).toString()));

            if (usuario.getId_usuario() == Integer.parseInt(jTabela_contas.getValueAt(opcao, 4).toString())) {

                ContasPagarDAO tdao = new ContasPagarDAO();
                tdao.excluir(c, usuario);
                preencherTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Não é possível excluir contas a pagar de outro usuário");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha.");
        }
    }//GEN-LAST:event_jBut_excluirActionPerformed

