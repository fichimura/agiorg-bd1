    //BOTAO SALVAR
    private void jB_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_salvarActionPerformed
        ContasPagar c = new ContasPagar();
        c.setDescricao(jT_descricao.getText());
        c.setQuantia(Double.parseDouble(jT_quantia.getText()));
        c.setData_conta(jF_data.getText());

        ContasPagarDAO cdao = new ContasPagarDAO();
        cdao.cadastrar(c, usuario);
        preencherTabela();
        jTabbedPane1.setSelectedIndex(1);
    }//GEN-LAST:event_jB_salvarActionPerformed

    //BOTAO ATUALIZAR
    private void jB_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_atualizarActionPerformed
        ContasPagar c = new ContasPagar();
        c.setId_conta(Integer.parseInt(jT_id.getText()));
        c.setDescricao(jT_descricao.getText());
        c.setQuantia(Double.parseDouble(jT_quantia.getText()));
        c.setData_conta(jF_data.getText());
        c.setFK_id_usuario(Integer.parseInt(jT_idusuario.getText()));

        if (usuario.getId_usuario() == Integer.parseInt(jT_idusuario.getText())) {
            ContasPagarDAO cdao = new ContasPagarDAO();
            cdao.atualizar(c, usuario);
            preencherTabela();
            jTabbedPane1.setSelectedIndex(1);
        } else {
            JOptionPane.showMessageDialog(null, "Não é possível atualizar conta a pagar de outro usuário.");
        }
    }//GEN-LAST:event_jB_atualizarActionPerformed

